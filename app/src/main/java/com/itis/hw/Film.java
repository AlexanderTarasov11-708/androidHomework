package com.itis.hw;

public class Film {
    public int id;
    public String title;
    public String year;
    public String description;
    public int poster;

    public Film(int id, String title, String year, String description, int poster) {
        this.id = id;
        this.title = title;
        this.year = year;
        this.description = description;
        this.poster = poster;
    }
}
